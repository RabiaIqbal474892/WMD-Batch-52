// Objects

// 7. Book Object
// Define an object representing a book with properties like title and author.

// Define a Book object
interface Book1 {
  title: string;
  author: string;
}

const myBook1: Book1 = {
  title: "My first book",
  author: "Rabiaa Iqbal",
};

// Accessing properties
console.log(`Title: ${myBook1.title}`);
console.log(`Author: ${myBook1.author}`);
